"""LiteLLM proxy custom callbacks.

Loaded by the homelab model gateway via:

    litellm_settings:
      callbacks: custom_callbacks.proxy_handler_instance

Two responsibilities, both implemented on the same logger object:

  1. **CostJsonlHandler** — append one JSON line per completed call to
     ``$LLM_CALLS_JSONL`` (default ``/var/log/llm-calls/llm-calls.jsonl``).
     This is the original Phase 0.6 behaviour.
  2. **Local-only enforcement** — Phase 1 finance prereq P1. The pre-call
     hook reads the ``x-skill`` header, looks up the skill in a snapshot of
     ``config/skills/`` (generated by ``generate_skill_policy.py`` at gateway
     start), and rejects the request with an LLM-friendly 403 if the skill
     is ``local_only=true`` but the requested model is not on a local route.
     An audit JSONL row with ``status="rejected_local_only"`` is appended so
     the cost relay surfaces the rejection in the master dashboard.

Design notes
------------
- Writes are best-effort and never raise into the request path. A logging
  failure here must never break an LLM call. The proxy will catch
  exceptions, but we belt-and-suspenders it with broad try/except.
- The record schema is the contract with ``apps/litellm_cost_relay/``;
  evolve via ``schema`` field bumps, not silent column renames.
- ``agent_principal`` is sourced from the ``x-agent-principal`` header set
  by ``apps/_shared/rlm/subcall.py``. Falls back to ``"unknown"``.
- ``request_id`` from the ``x-litellm-call-id`` header (proxy-assigned) so
  the same call can be cross-referenced in agent ledgers later.
- ``task_intent`` from the ``x-task-intent`` header (set by
  ``apps/_shared/rlm/subcall.py`` per sub-call). Lets us group cost/latency
  by task class (summarize / classify / code / plan) in the dashboard.
  Null when the caller doesn't supply it (e.g. direct API hits without
  the RLM harness).
- We extract ``response_cost`` opportunistically: LiteLLM auto-computes
  it for catalogued models, but our self-hosted vLLM models will yield
  ``0.0``. That is fine — token counts are the real signal locally.
"""

