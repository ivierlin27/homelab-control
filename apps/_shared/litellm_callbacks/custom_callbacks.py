# LiteLLM raises this from a pre-call hook to reject the request with a
# specific HTTP status. Importing lazily so the unit tests that don't have
# litellm installed can still load this module.
try:
    from fastapi import HTTPException  # type: ignore
except Exception:
    class HTTPException(Exception):  # type: ignore[no-redef]
        def __init__(self, status_code: int, detail: str) -> None:
            super().__init__(detail)
            self.status_code = status_code
            self.detail = detail


# ---------------------------------------------------------------------------
# Snapshot cache.
#
# The skill-policy snapshot is generated at gateway start (ExecStartPre on
# alienware-model-gateway.service). We load it lazily on first pre-call hook
# invocation and cache for the gateway process lifetime; restarting the unit
# refreshes the cache. The lazy load avoids forcing the snapshot to exist
# before the callback module imports (helps test environments).
# ---------------------------------------------------------------------------


_policy_lock = threading.Lock()
_policy_snapshot: PolicySnapshot | None = None


def _get_policy_snapshot() -> PolicySnapshot:
    global _policy_snapshot
    if _policy_snapshot is None:
        with _policy_lock:
            if _policy_snapshot is None:
                _policy_snapshot = load_snapshot()
    return _policy_snapshot


def _reset_policy_snapshot_for_tests() -> None:
    """Test-only: clear the cached snapshot so a new env can take effect."""
    global _policy_snapshot
    with _policy_lock:
        _policy_snapshot = None


def _audit_local_only_rejection(
    *,
    skill_id: str,
    model: str | None,
    reason: str,
    agent_principal: str | None,
    request_id: str | None,
) -> None:
    """Append a rejection row to the cost JSONL so the cost relay surfaces it."""
    try:
        record = {
            "schema": SCHEMA_VERSION,
            "ts": time.time(),
            "status": "rejected_local_only",
            "model": model,
            "agent_principal": agent_principal or "unknown",
            "task_intent": None,
            "request_id": request_id,
            "prompt_tokens": None,
            "completion_tokens": None,
            "total_tokens": None,
            "cost_usd": None,
            "latency_ms": 0,
            "user": None,
            "rejected_skill": skill_id,
            "error": reason[:512],
        }
        _append(record)
    except Exception as exc:  # noqa: BLE001
        print(
            f"[litellm-local-only] failed to write rejection audit: {exc}",
            file=sys.stderr,
            flush=True,
        )


class CostJsonlHandler(CustomLogger):  # type: ignore[misc]
    """Append a JSON record per LLM call to ``$LLM_CALLS_JSONL``."""

    # ---- Phase 1 P1: local-only enforcement (pre-call) -----------------
    async def async_pre_call_hook(  # type: ignore[override]
        self,
        user_api_key_dict: Any = None,
        cache: Any = None,
        data: Any = None,
        call_type: str = "",
    ) -> Any:
        """Reject the call before it's dispatched if it violates local_only.

        LiteLLM invokes this for every proxy request. ``data`` is the
        OpenAI-shaped request body (dict-ish). The proxy_server_request
        wrapper carries the original FastAPI request whose headers we need.

        Returning ``None``/``data`` proceeds; raising ``HTTPException``
        short-circuits with the given status code.
        """
        try:
            if not isinstance(data, Mapping):
                return data
            req = _proxy_request({"litellm_params": data}) or data
            # The "data" dict at the proxy hook is the request payload; the
            # original FastAPI request lives one level up. Try both shapes.
            headers = {}
            if isinstance(req, Mapping):
                hdrs = req.get("headers")
                if isinstance(hdrs, Mapping):
                    headers = hdrs
            # Some LiteLLM versions also stash the body under
            # data["proxy_server_request"]["headers"]; fall back to that.
            if not headers:
                psr = data.get("proxy_server_request") if isinstance(data, Mapping) else None
                if isinstance(psr, Mapping):
                    h2 = psr.get("headers")
                    if isinstance(h2, Mapping):
                        headers = h2
            skill_id = None
            agent_principal = None
            request_id = None
            for key, value in headers.items():
                if not isinstance(key, str) or not isinstance(value, str):
                    continue
                lk = key.lower()
                if lk == "x-skill":
                    skill_id = value
                elif lk == "x-agent-principal":
                    agent_principal = value
                elif lk in ("x-litellm-call-id", "x-request-id"):
                    request_id = request_id or value
            model = data.get("model") if isinstance(data, Mapping) else None
            snapshot = _get_policy_snapshot()
            check_call(skill_id=skill_id, model=model, snapshot=snapshot)
            return data
        except LocalOnlyViolation as exc:
            _audit_local_only_rejection(
                skill_id=exc.skill_id,
                model=exc.model,
                reason=exc.reason,
                agent_principal=agent_principal,
                request_id=request_id,
            )
            raise HTTPException(status_code=403, detail=exc.reason) from exc
        except HTTPException:
            raise
        except Exception as exc:  # noqa: BLE001 - never break the request path
            print(
                f"[litellm-local-only] pre-call hook crashed: {exc}; "
                f"fail-open (allowing call)",
                file=sys.stderr,
                flush=True,
            )
            return data

    async def async_log_success_event(  # type: ignore[override]
        self, kwargs: Mapping[str, Any], response_obj: Any, start_time: Any, end_time: Any
    ) -> None:
        try:
            record = build_record(kwargs, response_obj, start_time, end_time, status="success")
            _append(record)
        except Exception as exc:
            print(f"[litellm-cost-jsonl] success handler failed: {exc}", file=sys.stderr, flush=True)

    async def async_log_failure_event(  # type: ignore[override]
        self, kwargs: Mapping[str, Any], response_obj: Any, start_time: Any, end_time: Any
    ) -> None:
        try:
            err = kwargs.get("exception") or kwargs.get("error")
            record = build_record(
                kwargs,
                response_obj,
                start_time,
                end_time,
                status="failure",
                error=str(err) if err is not None else None,
            )
            _append(record)
        except Exception as exc:
            print(f"[litellm-cost-jsonl] failure handler failed: {exc}", file=sys.stderr, flush=True)

    def log_success_event(  # type: ignore[override]
        self, kwargs: Mapping[str, Any], response_obj: Any, start_time: Any, end_time: Any
    ) -> None:
        try:
            record = build_record(kwargs, response_obj, start_time, end_time, status="success")
            _append(record)
        except Exception as exc:
            print(f"[litellm-cost-jsonl] sync success handler failed: {exc}", file=sys.stderr, flush=True)

    def log_failure_event(  # type: ignore[override]
        self, kwargs: Mapping[str, Any], response_obj: Any, start_time: Any, end_time: Any
    ) -> None:
        try:
            err = kwargs.get("exception") or kwargs.get("error")
            record = build_record(
                kwargs,
                response_obj,
                start_time,
                end_time,
                status="failure",
                error=str(err) if err is not None else None,
            )
            _append(record)
        except Exception as exc:
            print(f"[litellm-cost-jsonl] sync failure handler failed: {exc}", file=sys.stderr, flush=True)


proxy_handler_instance = CostJsonlHandler()
